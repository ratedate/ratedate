class AvatarUploader < CarrierWave::Uploader::Base

  # Include RMagick or MiniMagick support:
  # include CarrierWave::RMagick
  include CarrierWave::MiniMagick

  # Choose what kind of storage to use for this uploader:
  storage :file
  # storage :fog

  # Override the directory where uploaded files will be stored.
  # This is a sensible default for uploaders that are meant to be mounted:
  def store_dir
    "uploads/#{model.class.to_s.underscore}/#{mounted_as}/#{model.id}"
  end

  version :large_avatar do
    process :crop_img
    process resize_to_fill: [760, 760]
  end

  version :medium_avatar, from_version: :large_avatar do
    process resize_to_fill: [200, 200]
  end

  version :small_avatar, from_version: :large_avatar do
    process resize_to_fill: [50,50]
  end

  def customize?
    !model.crop_x.blank? && !model.crop_y.blank? && !model.crop_w.blank? && !model.crop_h.blank?
  end

  def crop_img
    if customize?
      manipulate! do |img|
        x = model.crop_x.to_i
        y = model.crop_y.to_i
        w = model.crop_w.to_i
        h = model.crop_h.to_i
        img.crop("#{w}x#{h}+#{x}+#{y}")
        img
      end
    end
  end
